  most_skipped_category VARCHAR(100) DEFAULT NULL,
  total_estimated_minutes_completed INT DEFAULT 0,
  consistency_score DECIMAL(5,2) DEFAULT 0.00,
  summary_note TEXT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (user_id, plan_date)
);

CREATE TRIGGER trg_daily_evaluations_updated_at
BEFORE UPDATE ON daily_evaluations
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================
-- friend_requests
-- =========================
CREATE TABLE friend_requests (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sender_id INT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  receiver_id INT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  status VARCHAR(20) DEFAULT 'pending' CHECK (status IN ('pending','accepted','rejected','cancelled')),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (sender_id, receiver_id)
);
CREATE INDEX idx_friend_requests_receiver_id ON friend_requests(receiver_id);

CREATE TRIGGER trg_friend_requests_updated_at
BEFORE UPDATE ON friend_requests
FOR EACH ROW EXECUTE FUNCTION set_updated_at();

-- =========================
-- friends
-- =========================
CREATE TABLE friends (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  friend_id INT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  UNIQUE (user_id, friend_id)
);
CREATE INDEX idx_friends_friend_id ON friends(friend_id);

-- =========================
-- messages
-- =========================
CREATE TABLE messages (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sender_id INT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  receiver_id INT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  body TEXT NOT NULL,
  is_read BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
CREATE INDEX idx_messages_sender_id ON messages(sender_id);
CREATE INDEX idx_messages_receiver_id ON messages(receiver_id);

-- =========================
-- notifications
-- =========================
CREATE TABLE notifications (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
  type VARCHAR(30) DEFAULT 'system' CHECK (type IN ('task_reminder','overdue_alert','daily_review','collaboration','system')),
  title VARCHAR(150) NOT NULL,
  message TEXT NOT NULL,
  related_task_id INT DEFAULT NULL REFERENCES tasks(id) ON DELETE SET NULL,
  scheduled_for TIMESTAMP DEFAULT NULL,
  is_read BOOLEAN DEFAULT FALSE,
  is_sent BOOLEAN DEFAULT FALSE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  related_request_id INT DEFAULT NULL REFERENCES friend_requests(id) ON DELETE SET NULL
);
CREATE INDEX idx_notifications_user_id ON notifications(user_id);
CREATE INDEX idx_notifications_related_task_id ON notifications(related_task_id);
CREATE INDEX idx_notifications_related_request_id ON notifications(related_request_id);

CREATE TRIGGER trg_notifications_updated_at
BEFORE UPDATE ON notifications
FOR EACH ROW EXECUTE FUNCTION set_updated_at();
